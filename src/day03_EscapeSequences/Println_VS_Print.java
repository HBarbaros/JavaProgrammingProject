package day03_EscapeSequences;

public class Println_VS_Print {

    public static void main(String[] args) {

        System.out.println("Hello Cydeo");  // "system.out.println()" after printing the last character, appends new line
        System.out.println("How are you all today?");

        System.out.println("--------------------------");

        System.out.print("Hello Cydeo"); // "system.out.print()" after printing the last character,does not append new line
        System.out.print("How are you all today?");

        System.out.println("Java Programming");
        System.out.print("Wooden Spoon");

        System.out.println("I love Java");

    }

}

